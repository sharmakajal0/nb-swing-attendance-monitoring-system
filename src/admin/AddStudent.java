    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DbCon ob = new DbCon();
            String name = sname.getText();
            String roll = sroll.getText();
            String sems = sem.getText();
            String s1 = suba.getSelectedItem().toString();
            String s2 = subb.getSelectedItem().toString();
            String s3 = subc.getSelectedItem().toString();
            String s4 = subd.getSelectedItem().toString();
            
            String sql = "insert into students values('"+roll+"','"+name+"','"+sems+"','"+s1+"','"+s2+"','"+s3+"','"+s4+"')";
            int re = ob.dml(sql);
            if(re>0)
            {
                JOptionPane.showMessageDialog(null,"Saved successfully!!!");
            }

        } catch (Exception e ) {
            JOptionPane.showMessageDialog(null,"Error : "+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

