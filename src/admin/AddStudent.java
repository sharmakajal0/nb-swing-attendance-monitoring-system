    private void fillCombo()
    {
        try {
            DbCon dc = new DbCon();
            String stc = "SELECT * FROM subject;";
            ResultSet ft = dc.dql(stc);
            
            while(ft.next())
            {
                suba.addItem(ft.getString(2));
                subb.addItem(ft.getString(2));
                subc.addItem(ft.getString(2));
                subd.addItem(ft.getString(2));
            }
        } catch (Exception e) {
        }
    }
